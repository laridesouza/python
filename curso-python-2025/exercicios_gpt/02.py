# Crie uma lista com 5 notas de uma aluna.
# Calcule e imprima a média dessas notas
# Mostre a maior e a menor nota com max() e min()

notas = [6, 2.5, 8, 4.2, 9]

media = sum(notas) / len(notas)

print("Média das notas = ", media)
print("Maior nota = ", max(notas))
print("Menor nota = ", min(notas))
